"""
Crossbar Generator

Generates crossbar module that routes width-adapted signals from adapters to slaves.

The crossbar:
1. Takes multiple width-specific inputs from each adapter
2. Takes address decode one-hot signals (slave_select_aw/ar)
3. Selects correct width path based on target slave's width
4. Routes selected signals to slave outputs

Author: RTL Design Sherpa
Date: 2025-11-04
"""

from typing import List, Dict, Set
from dataclasses import dataclass

from bridge_pkg.generators.adapter_generator import MasterConfig, SlaveInfo
from bridge_pkg.signal_naming import SignalNaming, Direction, AXI4Channel, AXI4_SLAVE_SIGNALS, AXI4_MASTER_SIGNALS


class CrossbarGenerator:
    """
    Generates crossbar module for intelligent width-based routing.

    For each slave, routes signals from the correctly-sized path of each
    master that can target that slave.

    Example:
        Master A (64b) connects to Slave 0 (64b) and Slave 1 (128b)
        - Master A adapter outputs: 64b path, 128b path
        - Slave 0 gets: Master A 64b path (direct)
        - Slave 1 gets: Master A 128b path (converted)
    """

    def __init__(self, bridge_name: str, masters: List[MasterConfig], slaves: List[SlaveInfo]):
        """
        Initialize crossbar generator.

        Args:
            bridge_name: Name of bridge (e.g., "bridge_4x4_rw")
            masters: List of master configurations
            slaves: List of slave configurations
        """
        self.bridge_name = bridge_name
        self.masters = masters
        self.slaves = slaves

    def generate(self) -> str:
        """Generate complete crossbar module."""
        lines = []

        # Header
        lines.extend(self._generate_header())

        # Module declaration
        lines.extend(self._generate_module_declaration())

        # Crossbar routing logic
        lines.extend(self._generate_routing_logic())

        # Module end
        lines.append(f"endmodule : {self.bridge_name}_xbar")
        lines.append("")

        return "\n".join(lines)

    def _generate_header(self) -> List[str]:
        """Generate file header."""
        return [
            f"// Crossbar: {self.bridge_name}_xbar",
            "// Generated by: CrossbarGenerator",
            "// ",
            "// Routes width-adapted signals from adapters to slaves.",
            "// Selects correct width path based on address decode.",
            "",
            "`timescale 1ns / 1ps",
            "",
            f"import {self.bridge_name}_pkg::*;",
            ""
        ]

    def _generate_module_declaration(self) -> List[str]:
        """Generate module declaration with all ports."""
        lines = []

        lines.append(f"module {self.bridge_name}_xbar (")
        lines.append("    input  logic aclk,")
        lines.append("    input  logic aresetn,")
        lines.append("")

        # Adapter inputs (multiple width paths per master)
        for master_idx, master in enumerate(self.masters):
            is_last_master = (master_idx == len(self.masters) - 1)
            has_slaves = (len(self.slaves) > 0)
            lines.append(f"    // {master.name} adapter outputs (multiple width paths)")
            lines.extend(self._generate_adapter_input_ports(master, is_last_master, has_slaves))

            # Add comma if not last master or if slaves follow
            if not is_last_master or has_slaves:
                lines.append("")

        # Slave outputs
        for slave_idx, slave in enumerate(self.slaves):
            lines.append(f"    // Slave {slave_idx}: {slave.name}")
            lines.extend(self._generate_slave_output_ports(slave))

            # Add comma if not last slave
            if slave_idx < len(self.slaves) - 1:
                lines.append("")

        lines.append(");")
        lines.append("")

        return lines

    def _generate_adapter_input_ports(self, master: MasterConfig, is_last_master: bool, has_slaves: bool) -> List[str]:
        """
        Generate input ports from adapter for one master.

        Args:
            master: Master configuration
            is_last_master: True if this is the last master in the list
            has_slaves: True if there are slave ports following

        Includes:
        - Address decode one-hot signals
        - Width-specific channel signals (one set per unique slave width)
        """
        lines = []

        # Get unique slave widths this master connects to
        slave_widths = self._get_connected_slave_widths(master)

        # Address decode signals (for request routing)
        if master.channels in ["wr", "rw"]:
            lines.append(f"    input  logic [NUM_SLAVES-1:0] {master.name}_slave_select_aw,")
            lines.append(f"    input  logic [BRIDGE_ID_WIDTH-1:0] {master.name}_bridge_id_aw,")
        if master.channels in ["rd", "rw"]:
            lines.append(f"    input  logic [NUM_SLAVES-1:0] {master.name}_slave_select_ar,")
            lines.append(f"    input  logic [BRIDGE_ID_WIDTH-1:0] {master.name}_bridge_id_ar,")

        # Width-specific signals for each unique width
        for width_idx, width in enumerate(slave_widths):
            suffix = f"{width}b"
            is_last_width = (width_idx == len(slave_widths) - 1)
            # This port needs comma unless it's the VERY last port in the entire module
            needs_comma = not (is_last_master and is_last_width and not has_slaves)

            lines.append(f"    // {width}b path")

            # Write channels
            if master.channels in ["wr", "rw"]:
                lines.append(f"    input  axi4_aw_t     {master.name}_{suffix}_aw,")
                lines.append(f"    input  logic         {master.name}_{suffix}_awvalid,")
                lines.append(f"    output logic         {master.name}_{suffix}_awready,")
                lines.append(f"    input  axi4_w_{suffix}_t  {master.name}_{suffix}_w,")
                lines.append(f"    input  logic         {master.name}_{suffix}_wvalid,")
                lines.append(f"    output logic         {master.name}_{suffix}_wready,")
                lines.append(f"    output axi4_b_t      {master.name}_{suffix}_b,")
                lines.append(f"    output logic         {master.name}_{suffix}_bvalid,")
                # Determine if comma needed
                if master.channels == "wr" and is_last_width:
                    # This is last signal for write-only master
                    if needs_comma:
                        lines.append(f"    input  logic         {master.name}_{suffix}_bready,")
                    else:
                        lines.append(f"    input  logic         {master.name}_{suffix}_bready")
                else:
                    lines.append(f"    input  logic         {master.name}_{suffix}_bready,")

            # Read channels
            if master.channels in ["rd", "rw"]:
                lines.append(f"    input  axi4_ar_t     {master.name}_{suffix}_ar,")
                lines.append(f"    input  logic         {master.name}_{suffix}_arvalid,")
                lines.append(f"    output logic         {master.name}_{suffix}_arready,")
                lines.append(f"    output axi4_r_{suffix}_t  {master.name}_{suffix}_r,")
                lines.append(f"    output logic         {master.name}_{suffix}_rvalid,")
                # Determine if comma needed (rready is always last for rd/rw channels)
                if is_last_width:
                    # This is last signal for this master
                    if needs_comma:
                        lines.append(f"    input  logic         {master.name}_{suffix}_rready,")
                    else:
                        lines.append(f"    input  logic         {master.name}_{suffix}_rready")
                else:
                    lines.append(f"    input  logic         {master.name}_{suffix}_rready,")

        return lines

    def _determine_bridge_channels(self) -> List[AXI4Channel]:
        """
        Determine which AXI4 channels the bridge needs based on masters.

        Returns list of channels needed:
        - If ANY master has wr/rw: include AW, W, B
        - If ANY master has rd/rw: include AR, R
        """
        has_write = any(m.channels in ["wr", "rw"] for m in self.masters)
        has_read = any(m.channels in ["rd", "rw"] for m in self.masters)

        channels = []
        if has_write:
            channels.extend([AXI4Channel.AW, AXI4Channel.W, AXI4Channel.B])
        if has_read:
            channels.extend([AXI4Channel.AR, AXI4Channel.R])

        return channels

    def _determine_slave_channels(self, slave: SlaveInfo) -> List[AXI4Channel]:
        """
        Determine which AXI4 channels THIS SPECIFIC SLAVE needs based on
        the masters that connect to it.

        Args:
            slave: Slave to determine channels for

        Returns:
            List of channels needed for this slave:
            - If ANY connecting master has wr/rw: include AW, W, B
            - If ANY connecting master has rd/rw: include AR, R
        """
        # Get masters that connect to this slave
        connecting_masters = self._get_masters_connecting_to_slave(slave)

        if not connecting_masters:
            # No masters connect - shouldn't happen, but return empty for safety
            return []

        # Check if any connecting master needs write channels
        has_write = any(m.channels in ["wr", "rw"] for m in connecting_masters)
        # Check if any connecting master needs read channels
        has_read = any(m.channels in ["rd", "rw"] for m in connecting_masters)

        channels = []
        if has_write:
            channels.extend([AXI4Channel.AW, AXI4Channel.W, AXI4Channel.B])
        if has_read:
            channels.extend([AXI4Channel.AR, AXI4Channel.R])

        return channels

    def _generate_slave_output_ports(self, slave: SlaveInfo) -> List[str]:
        """
        Generate output ports to slave using SignalNaming convention.

        IMPORTANT:
        - Crossbar acts as MASTER to the slave, so port directions are from master perspective
        - Uses SignalNaming convention: <port_name>_axi_<channel><signal> (no direction prefix)
        - Only generates channels needed by THIS SPECIFIC SLAVE based on connecting masters
        """
        lines = []

        # Use SignalNaming convention: <port_name>_axi_<channel><signal>
        prefix = f"{slave.name}_axi_"

        # Determine correct interface width (for APB slaves, use connecting master's width)
        data_width, addr_width = self._determine_slave_interface_width(slave)

        # AXI4 *id width is master pass-through, not a fixed crossbar constant
        # (Bug B in TASK-011 — was hardcoded to 4). Floor at 1 to avoid the
        # invalid `[-1:0]` SV range when id_width=0 (Bug A).
        master_id_width = max(m.id_width for m in self.masters) if self.masters else 4
        master_id_width = max(master_id_width, 1)

        # Width parameters for signal info queries
        width_values = {
            'ID_WIDTH': master_id_width,
            'ADDR_WIDTH': addr_width,
            'DATA_WIDTH': data_width,
            'STRB_WIDTH': data_width // 8,
            'USER_WIDTH': 1
        }

        # Get channels needed by THIS SLAVE (based on connecting masters' channel types)
        channels = self._determine_slave_channels(slave)
        has_write = any(c in [AXI4Channel.AW, AXI4Channel.W, AXI4Channel.B] for c in channels)
        has_read = any(c in [AXI4Channel.AR, AXI4Channel.R] for c in channels)

        # Add bridge_id signals (from slave adapter, for response routing)
        if has_write:
            lines.append(f"    output logic [BRIDGE_ID_WIDTH-1:0] {slave.name}_axi_bridge_id_aw,")
            lines.append(f"    input  logic [BRIDGE_ID_WIDTH-1:0] {slave.name}_axi_bid_bridge_id,")
            lines.append(f"    input  logic                       {slave.name}_axi_bid_valid,")
        if has_read:
            if has_write:
                lines.append("")
            lines.append(f"    output logic [BRIDGE_ID_WIDTH-1:0] {slave.name}_axi_bridge_id_ar,")
            lines.append(f"    input  logic [BRIDGE_ID_WIDTH-1:0] {slave.name}_axi_rid_bridge_id,")
            lines.append(f"    input  logic                       {slave.name}_axi_rid_valid,")
        lines.append("")

        # Get signal info from SignalNaming (crossbar is master to slave)
        signal_db = AXI4_MASTER_SIGNALS  # Crossbar acts as MASTER to slaves (outputs request, inputs response)

        # Generate declarations for each channel
        first_channel = True

        # Pre-calculate which is the last signal for comma handling
        is_last_slave = (slave == self.slaves[-1])
        last_channel = channels[-1] if channels else None

        for channel_idx, channel in enumerate(channels):
            if not first_channel:
                lines.append("")
            first_channel = False

            if channel not in signal_db:
                continue

            channel_signals = list(signal_db[channel])
            for sig_idx, sig_info in enumerate(channel_signals):
                # Build signal name using prefix + channel + signal
                sig_name = f"{prefix}{channel.value}{sig_info.name}"
                # Get complete signal declaration
                declaration = sig_info.get_declaration(sig_name, width_values)

                # Check if this is the VERY last signal in the module port list
                is_last_channel = (channel == last_channel)
                is_last_signal_in_channel = (sig_idx == len(channel_signals) - 1)
                is_very_last_signal = (is_last_slave and is_last_channel and is_last_signal_in_channel)

                if is_very_last_signal:
                    lines.append(f"    {declaration}")  # No comma
                else:
                    lines.append(f"    {declaration},")

        return lines

    def _generate_routing_logic(self) -> List[str]:
        """
        Generate crossbar routing logic.

        For each slave:
        1. Determine the slave's data width
        2. For each master that connects to this slave:
           - Select the width path matching slave's width
           - Route based on address decode one-hot
        """
        lines = []

        lines.append("    // ================================================================")
        lines.append("    // Crossbar Routing")
        lines.append("    // ================================================================")
        lines.append(f"    localparam NUM_SLAVES = {len(self.slaves)};")
        lines.append("")

        # Route to each slave
        for slave_idx, slave in enumerate(self.slaves):
            lines.extend(self._generate_slave_routing(slave_idx, slave))
            lines.append("")

        # Generate response MUXes (OR together responses from all slaves)
        lines.extend(self._generate_response_muxes())

        return lines

    def _generate_slave_routing(self, slave_idx: int, slave: SlaveInfo) -> List[str]:
        """
        Generate routing logic for one slave.

        Selects correct width path from each master based on:
        - Whether master connects to this slave
        - Slave's data width
        - Address decode one-hot signal
        """
        lines = []
        slave_width = slave.data_width
        slave_suffix = f"{slave_width}b"

        lines.append(f"    // ================================================================")
        lines.append(f"    // Slave {slave_idx}: {slave.name} ({slave_width}b)")
        lines.append(f"    // ================================================================")

        # Find masters that connect to this slave
        connecting_masters = []
        for master in self.masters:
            if slave_idx in master.slave_connections:
                connecting_masters.append(master)

        if len(connecting_masters) == 0:
            lines.append(f"    // No masters connect to {slave.name}")
            # Use SignalNaming convention for signal names
            prefix = f"{slave.name}_axi_"
            # Only assign signals for channels that THIS SLAVE needs
            slave_channels = self._determine_slave_channels(slave)
            if AXI4Channel.AW in slave_channels:
                lines.append(f"    assign {prefix}awvalid = 1'b0;")
            if AXI4Channel.W in slave_channels:
                lines.append(f"    assign {prefix}wvalid = 1'b0;")
            if AXI4Channel.AR in slave_channels:
                lines.append(f"    assign {prefix}arvalid = 1'b0;")
            return lines

        # For single master case (simplified routing)
        if len(connecting_masters) == 1:
            master = connecting_masters[0]
            lines.extend(self._generate_single_master_routing(slave_idx, slave, master))
        else:
            # Multi-master: OR-merge each master's contribution to the
            # slave-side request signals, gated by that master's own
            # `slave_select_aw/ar[slave_idx]`. This is correct as long as
            # only one master at a time is targeting this slave (which is
            # what the basic_connectivity test exercises). Simultaneous
            # contention would need a real arbiter; that's a separate
            # follow-up. Response-side routing already uses the
            # slave_adapter's bid_bridge_id/rid_bridge_id tracking
            # to pick the right destination master.
            lines.extend(self._generate_multi_master_routing(slave_idx, slave, connecting_masters))

        return lines

    def _generate_multi_master_routing(self, slave_idx: int, slave: SlaveInfo,
                                        masters: List[MasterConfig]) -> List[str]:
        """OR-merge each master's request contribution onto the slave-side
        request signals. Use the slave's bid_*/rid_* tracking to route the
        response back through the correct master."""
        lines = []
        suffix = f"{slave.data_width}b"
        prefix = f"{slave.name}_axi_"

        wr_masters = [m for m in masters if m.channels in ("wr", "rw")]
        rd_masters = [m for m in masters if m.channels in ("rd", "rw")]

        lines.append(f"    // Multi-master ({len(masters)} masters) → {slave.name}")
        for m in masters:
            lines.append(f"    //   - {m.name} ({m.channels})")
        lines.append("")

        # Per-master address-decode signals. Defined once up-front and
        # reused by every OR-merge below so we don't repeat the same
        # comparison expression.
        for m in masters:
            if m.channels in ("wr", "rw"):
                aw_dec = self._addr_decode_expr(m.name, suffix, "aw", slave)
                lines.append(f"    wire {m.name}_{suffix}_aw_to_{slave.name} = {aw_dec};")
            if m.channels in ("rd", "rw"):
                ar_dec = self._addr_decode_expr(m.name, suffix, "ar", slave)
                lines.append(f"    wire {m.name}_{suffix}_ar_to_{slave.name} = {ar_dec};")
        lines.append("")

        def _or_merge(out_sig: str, struct_field: str | None,
                      master_sig_template: str, sel_array: str,
                      master_subset: List[MasterConfig],
                      valid_signal: str) -> str:
            """Build an OR-merged assignment line.

            Each OR-merged term is gated by `addr_decode && valid` so
            an idle master with a stale fub_axi_*addr does NOT contribute.
            Address decode happens on the converter/passthrough's m_axi
            address bus (stable across the entire handshake), avoiding
            the slave_select_* reverts-after-handshake bug.
            """
            parts = []
            for m in master_subset:
                base = master_sig_template.format(m=m.name, suffix=suffix)
                lhs = f"{base}{struct_field}" if struct_field else base
                sel = f"{m.name}_{suffix}_{sel_array}_to_{slave.name}"
                vld = valid_signal.format(m=m.name, suffix=suffix)
                parts.append(f"(({sel} && {vld}) ? {lhs} : '0)")
            return " |\n        ".join(parts) if parts else "'0"

        # Write side. Each OR-merge term is gated by `slave_select_aw &&
        # awvalid` (or wvalid for W) so an idle master with a stale
        # `fub_axi_awaddr` that happens to decode to slave_idx does NOT
        # contribute. The W beats use the same `slave_select_aw` (which
        # holds across the W phase), gated by wvalid for liveness.
        aw_valid = "{m}_{suffix}_awvalid"
        w_valid = "{m}_{suffix}_wvalid"
        ar_valid = "{m}_{suffix}_arvalid"

        if wr_masters:
            lines.append("    // AW channel (OR-merged across writing masters)")
            for sig, fld in [("awid", ".id"), ("awaddr", ".addr"), ("awlen", ".len"),
                             ("awsize", ".size"), ("awburst", ".burst"),
                             ("awlock", ".lock"), ("awcache", ".cache"),
                             ("awprot", ".prot")]:
                tmpl = "{m}_{suffix}_aw"
                expr = _or_merge(sig, fld, tmpl, "aw", wr_masters, aw_valid)
                lines.append(f"    assign {prefix}{sig} = {expr};")
            tmpl = "{m}_{suffix}_awvalid"
            lines.append(f"    assign {prefix}awvalid = {_or_merge('awvalid', None, tmpl, 'aw', wr_masters, aw_valid)};")
            lines.append("")

            # Per-(master,slave) AW->W tracking FIFO: capture which slave
            # the AW was driven to so W beats follow even after the AW
            # address moves on. Same pattern as single-master.
            for m in wr_masters:
                w_sig = f"{m.name}_{suffix}_w_to_{slave.name}"
                aw_sig = f"{m.name}_{suffix}_aw_to_{slave.name}"
                lines.append(f"    // AW->W tracking FIFO: {m.name} -> {slave.name}")
                lines.append(f"    logic {w_sig};")
                lines.append(f"    logic [3:0] {aw_sig}_w_wptr, {aw_sig}_w_rptr;")
                lines.append(f"    logic {aw_sig}_w_mem [16];")
                lines.append(f"    logic {aw_sig}_w_push, {aw_sig}_w_pop;")
                lines.append(f"    assign {aw_sig}_w_push = {m.name}_{suffix}_awvalid && {m.name}_{suffix}_awready && {aw_sig};")
                lines.append(f"    assign {aw_sig}_w_pop  = {m.name}_{suffix}_wvalid && {m.name}_{suffix}_wready && {m.name}_{suffix}_w.last && {w_sig};")
                lines.append(f"    always_ff @(posedge aclk or negedge aresetn) begin")
                lines.append(f"        if (!aresetn) begin")
                lines.append(f"            {aw_sig}_w_wptr <= '0;")
                lines.append(f"            {aw_sig}_w_rptr <= '0;")
                lines.append(f"        end else begin")
                lines.append(f"            if ({aw_sig}_w_push) begin")
                lines.append(f"                {aw_sig}_w_mem[{aw_sig}_w_wptr] <= 1'b1;")
                lines.append(f"                {aw_sig}_w_wptr <= {aw_sig}_w_wptr + 1'b1;")
                lines.append(f"            end")
                lines.append(f"            if ({aw_sig}_w_pop) begin")
                lines.append(f"                {aw_sig}_w_rptr <= {aw_sig}_w_rptr + 1'b1;")
                lines.append(f"            end")
                lines.append(f"        end")
                lines.append(f"    end")
                lines.append(f"    assign {w_sig} = ({aw_sig}_w_wptr != {aw_sig}_w_rptr) ? {aw_sig}_w_mem[{aw_sig}_w_rptr] : 1'b0;")
                lines.append("")

            lines.append("    // W channel (OR-merged across writing masters, gated by w_to_<slave> FIFO)")
            for sig, fld in [("wdata", ".data"), ("wstrb", ".strb"), ("wlast", ".last")]:
                tmpl = "{m}_{suffix}_w"
                expr = _or_merge(sig, fld, tmpl, "w", wr_masters, w_valid)
                lines.append(f"    assign {prefix}{sig} = {expr};")
            tmpl = "{m}_{suffix}_wvalid"
            lines.append(f"    assign {prefix}wvalid = {_or_merge('wvalid', None, tmpl, 'w', wr_masters, w_valid)};")
            lines.append("")

            # bready: route back to whichever master owns this response
            # (per bid_bridge_id from the slave_adapter). bid_valid keeps
            # the path open across the whole B handshake.
            lines.append("    // Bready (slave → owning master, by bid_bridge_id)")
            bready_terms = []
            for m in wr_masters:
                idx = self.masters.index(m)
                bready_terms.append(
                    f"(({prefix}bid_bridge_id == {idx}) && {prefix}bid_valid ? {m.name}_{suffix}_bready : '0)"
                )
            lines.append(f"    assign {prefix}bready = " + " |\n        ".join(bready_terms) + ";")
            lines.append("")

            # bridge_id_aw: which master originated this AW. Gate on
            # addr-decode && awvalid (the address bus is stable across
            # the AW handshake; slave_select_aw isn't).
            lines.append("    // Bridge ID (writes) — picks the originating master's id")
            bid_terms = []
            for m in wr_masters:
                bid_terms.append(
                    f"(({m.name}_{suffix}_aw_to_{slave.name} && {m.name}_{suffix}_awvalid) ? {m.name}_bridge_id_aw : '0)"
                )
            lines.append(f"    assign {prefix}bridge_id_aw = " + " |\n        ".join(bid_terms) + ";")
            lines.append("")

        # Read side. Same `slave_select && arvalid` gating story.
        if rd_masters:
            lines.append("    // AR channel (OR-merged across reading masters)")
            for sig, fld in [("arid", ".id"), ("araddr", ".addr"), ("arlen", ".len"),
                             ("arsize", ".size"), ("arburst", ".burst"),
                             ("arlock", ".lock"), ("arcache", ".cache"),
                             ("arprot", ".prot")]:
                tmpl = "{m}_{suffix}_ar"
                expr = _or_merge(sig, fld, tmpl, "ar", rd_masters, ar_valid)
                lines.append(f"    assign {prefix}{sig} = {expr};")
            tmpl = "{m}_{suffix}_arvalid"
            lines.append(f"    assign {prefix}arvalid = {_or_merge('arvalid', None, tmpl, 'ar', rd_masters, ar_valid)};")
            lines.append("")

            lines.append("    // Rready (slave → owning master, by rid_bridge_id)")
            rready_terms = []
            for m in rd_masters:
                idx = self.masters.index(m)
                rready_terms.append(
                    f"(({prefix}rid_bridge_id == {idx}) && {prefix}rid_valid ? {m.name}_{suffix}_rready : '0)"
                )
            lines.append(f"    assign {prefix}rready = " + " |\n        ".join(rready_terms) + ";")
            lines.append("")

            lines.append("    // Bridge ID (reads) — picks the originating master's id")
            rid_terms = []
            for m in rd_masters:
                rid_terms.append(
                    f"(({m.name}_{suffix}_ar_to_{slave.name} && {m.name}_{suffix}_arvalid) ? {m.name}_bridge_id_ar : '0)"
                )
            lines.append(f"    assign {prefix}bridge_id_ar = " + " |\n        ".join(rid_terms) + ";")
            lines.append("")

        return lines

    def _generate_single_master_routing(self, slave_idx: int, slave: SlaveInfo,
                                       master: MasterConfig) -> List[str]:
        """
        Generate routing for slave with single master.

        Directly connects master's width-matched path to slave.
        For APB slaves, uses LCD width instead of slave native width.
        """
        lines = []

        # Determine adapter output width for this slave connection
        # For ALL slaves (both APB and AXI4), use the slave's native data width
        # The master adapter handles width conversion to produce correctly-sized paths
        adapter_width = slave.data_width

        suffix = f"{adapter_width}b"

        lines.append(f"    // Single master: {master.name} → {slave.name}")
        lines.append(f"    // Master width: {master.data_width}b, Slave width: {slave.data_width}b")
        lines.append(f"    // Using {suffix} path from adapter (APB LCD width)" if slave.protocol == 'apb' else f"    // Using {suffix} path from adapter")
        lines.append("")

        # Write channels
        if master.channels in ["wr", "rw"]:
            lines.extend(self._generate_write_channel_routing(slave_idx, slave, master, suffix))

        # Read channels
        if master.channels in ["rd", "rw"]:
            lines.extend(self._generate_read_channel_routing(slave_idx, slave, master, suffix))

        return lines

    def _addr_decode_expr(self, master_name: str, suffix: str, channel: str,
                          slave: SlaveInfo) -> str:
        """Return an expression evaluating to 1 when
        `<master_name>_<suffix>_<channel>.addr` falls in `slave`'s range.
        Used in place of `<master>_slave_select_<channel>[slave_idx]` to
        gate AR/AW signals: the address bus on the converter/passthrough
        m_axi side is held stable across the entire handshake, while the
        master adapter's `slave_select_*` reverts the moment fub_axi
        `*addr` reverts -- which happens as soon as the wrapper pops the
        skid, before the converter has finished pushing to the xbar.
        """
        addr_sig = f"{master_name}_{suffix}_{channel}.addr"
        base = slave.base_addr
        end = slave.base_addr + slave.addr_range - 1
        aw = slave.addr_width
        hex_digits = (aw + 3) // 4
        max_addr = (1 << aw) - 1
        # Verilator warns when comparisons are tautological for an
        # unsigned bus. Drop the always-true halves to keep the RTL
        # lint-clean. If the slave covers the full address space, the
        # decode is just a constant 1.
        base_zero = (base == 0)
        end_max = (end >= max_addr)
        if base_zero and end_max:
            return "1'b1"
        if base_zero:
            return f"({addr_sig} <= {aw}'h{end:0{hex_digits}x})"
        if end_max:
            return f"({addr_sig} >= {aw}'h{base:0{hex_digits}x})"
        return (f"(({addr_sig} >= {aw}'h{base:0{hex_digits}x}) && "
                f"({addr_sig} <= {aw}'h{end:0{hex_digits}x}))")

    def _generate_write_channel_routing(self, slave_idx: int, slave: SlaveInfo,
                                        master: MasterConfig, suffix: str) -> List[str]:
        """Generate AW, W, B channel routing.

        AW gating uses address re-decode on `<master>_<suffix>_aw.addr`
        (the converter/passthrough's m_axi side) -- see _addr_decode_expr
        for rationale. W follows AW via a small per-(master,slave) FIFO
        that captures which slave the AW went to at AW handshake and
        gates W beats until the WLAST handshake pops the FIFO.
        """
        lines = []
        # Use SignalNaming convention for slave signals
        prefix = f"{slave.name}_axi_"
        master_idx = self.masters.index(master)
        addr_dec = self._addr_decode_expr(master.name, suffix, "aw", slave)
        sig_select = f"{master.name}_{suffix}_aw_to_{slave.name}"
        w_sig = f"{master.name}_{suffix}_w_to_{slave.name}"

        # AW channel (master → slave) - address-decode gating
        lines.append("    // AW channel (gated by address re-decode -- see _addr_decode_expr)")
        lines.append(f"    wire {sig_select} = {addr_dec};")
        lines.append(f"    assign {prefix}awid     = {sig_select} ? {master.name}_{suffix}_aw.id : '0;")
        lines.append(f"    assign {prefix}awaddr   = {sig_select} ? {master.name}_{suffix}_aw.addr : '0;")
        lines.append(f"    assign {prefix}awlen    = {sig_select} ? {master.name}_{suffix}_aw.len : '0;")
        lines.append(f"    assign {prefix}awsize   = {sig_select} ? {master.name}_{suffix}_aw.size : '0;")
        lines.append(f"    assign {prefix}awburst  = {sig_select} ? {master.name}_{suffix}_aw.burst : '0;")
        lines.append(f"    assign {prefix}awlock   = {sig_select} ? {master.name}_{suffix}_aw.lock : '0;")
        lines.append(f"    assign {prefix}awcache  = {sig_select} ? {master.name}_{suffix}_aw.cache : '0;")
        lines.append(f"    assign {prefix}awprot   = {sig_select} ? {master.name}_{suffix}_aw.prot : '0;")
        lines.append(f"    assign {prefix}awvalid  = {sig_select} && {master.name}_{suffix}_awvalid;")
        lines.append("")

        # AW->W tracking FIFO so W beats follow the AW.
        lines.append(f"    // AW->W tracking FIFO for this (master,slave) pair")
        lines.append(f"    logic {w_sig};")
        lines.append(f"    logic [3:0] {sig_select}_w_wptr, {sig_select}_w_rptr;")
        lines.append(f"    logic {sig_select}_w_mem [16];")
        lines.append(f"    logic {sig_select}_w_push, {sig_select}_w_pop;")
        lines.append(f"    assign {sig_select}_w_push = {master.name}_{suffix}_awvalid && {master.name}_{suffix}_awready && {sig_select};")
        lines.append(f"    assign {sig_select}_w_pop  = {master.name}_{suffix}_wvalid && {master.name}_{suffix}_wready && {master.name}_{suffix}_w.last && {w_sig};")
        lines.append(f"    always_ff @(posedge aclk or negedge aresetn) begin")
        lines.append(f"        if (!aresetn) begin")
        lines.append(f"            {sig_select}_w_wptr <= '0;")
        lines.append(f"            {sig_select}_w_rptr <= '0;")
        lines.append(f"        end else begin")
        lines.append(f"            if ({sig_select}_w_push) begin")
        lines.append(f"                {sig_select}_w_mem[{sig_select}_w_wptr] <= 1'b1;")
        lines.append(f"                {sig_select}_w_wptr <= {sig_select}_w_wptr + 1'b1;")
        lines.append(f"            end")
        lines.append(f"            if ({sig_select}_w_pop) begin")
        lines.append(f"                {sig_select}_w_rptr <= {sig_select}_w_rptr + 1'b1;")
        lines.append(f"            end")
        lines.append(f"        end")
        lines.append(f"    end")
        lines.append(f"    assign {w_sig} = ({sig_select}_w_wptr != {sig_select}_w_rptr) ? {sig_select}_w_mem[{sig_select}_w_rptr] : 1'b0;")
        lines.append("")
        lines.append("")

        # W channel (master → slave)
        lines.append("    // W channel (gated by aw_to_<slave> FIFO head)")
        lines.append(f"    assign {prefix}wdata  = {w_sig} ? {master.name}_{suffix}_w.data : '0;")
        lines.append(f"    assign {prefix}wstrb  = {w_sig} ? {master.name}_{suffix}_w.strb : '0;")
        lines.append(f"    assign {prefix}wlast  = {w_sig} ? {master.name}_{suffix}_w.last : '0;")
        lines.append(f"    assign {prefix}wvalid = {w_sig} && {master.name}_{suffix}_wvalid;")
        lines.append("")

        # bready (master → slave). Use the slave's bid_valid/bid_bridge_id
        # tracking instead of slave_select_aw, because slave_select_aw is
        # combinational from fub_axi_awaddr and goes back to 0 once the AW
        # has been issued — leaving bready stuck at 0 while the slave is
        # still trying to drive bvalid for the response. Earlier versions
        # had this exact bug.
        lines.append("    // Bready (master → slave) — gated on bid_valid so the path stays")
        lines.append("    // open through the entire B handshake, not just the AW phase.")
        lines.append(f"    assign {prefix}bready = (({prefix}bid_bridge_id == {master_idx}) && {prefix}bid_valid) ? {master.name}_{suffix}_bready : '0;")
        lines.append("")

        # Bridge ID routing (master → slave) - passes master index for response routing
        lines.append("    // Bridge ID (master → slave)")
        lines.append(f"    assign {prefix}bridge_id_aw = {sig_select} ? {master.name}_bridge_id_aw : '0;")
        lines.append("")

        return lines

    def _generate_read_channel_routing(self, slave_idx: int, slave: SlaveInfo,
                                       master: MasterConfig, suffix: str) -> List[str]:
        """Generate AR, R channel routing.

        AR gating uses address re-decode on `<master>_<suffix>_ar.addr`
        (the converter/passthrough's m_axi side). See _addr_decode_expr.
        """
        lines = []
        # Use SignalNaming convention for slave signals
        prefix = f"{slave.name}_axi_"
        master_idx = self.masters.index(master)
        addr_dec = self._addr_decode_expr(master.name, suffix, "ar", slave)
        sig_select = f"{master.name}_{suffix}_ar_to_{slave.name}"

        # AR channel (master → slave) - address-decode gating
        lines.append("    // AR channel (gated by address re-decode -- see _addr_decode_expr)")
        lines.append(f"    wire {sig_select} = {addr_dec};")
        lines.append(f"    assign {prefix}arid     = {sig_select} ? {master.name}_{suffix}_ar.id : '0;")
        lines.append(f"    assign {prefix}araddr   = {sig_select} ? {master.name}_{suffix}_ar.addr : '0;")
        lines.append(f"    assign {prefix}arlen    = {sig_select} ? {master.name}_{suffix}_ar.len : '0;")
        lines.append(f"    assign {prefix}arsize   = {sig_select} ? {master.name}_{suffix}_ar.size : '0;")
        lines.append(f"    assign {prefix}arburst  = {sig_select} ? {master.name}_{suffix}_ar.burst : '0;")
        lines.append(f"    assign {prefix}arlock   = {sig_select} ? {master.name}_{suffix}_ar.lock : '0;")
        lines.append(f"    assign {prefix}arcache  = {sig_select} ? {master.name}_{suffix}_ar.cache : '0;")
        lines.append(f"    assign {prefix}arprot   = {sig_select} ? {master.name}_{suffix}_ar.prot : '0;")
        lines.append(f"    assign {prefix}arvalid  = {sig_select} && {master.name}_{suffix}_arvalid;")
        lines.append("")

        # rready (master → slave). Same fix as bready: gate on rid_valid /
        # rid_bridge_id (which the slave_adapter holds combinationally
        # across the response phase) instead of slave_select_ar (which
        # is combinational from fub_axi_araddr and goes back to 0 once
        # the AR has been issued — so the slave never gets rready and
        # the R handshake stalls).
        lines.append("    // Rready (master → slave) — gated on rid_valid so the path stays")
        lines.append("    // open through the entire R handshake, not just the AR phase.")
        lines.append(f"    assign {prefix}rready = (({prefix}rid_bridge_id == {master_idx}) && {prefix}rid_valid) ? {master.name}_{suffix}_rready : '0;")
        lines.append("")

        # Bridge ID routing (master → slave) - passes master index for response routing
        lines.append("    // Bridge ID (master → slave)")
        lines.append(f"    assign {prefix}bridge_id_ar = {sig_select} ? {master.name}_bridge_id_ar : '0;")
        lines.append("")

        return lines

    def _generate_response_muxes(self) -> List[str]:
        """
        Generate response MUX logic to OR together slave responses.

        For each master and width combination, generate OR-based muxing of:
        - Write responses: awready, wready, b.* (uses slave_select_aw)
        - Read responses: arready, r.* (uses slave_select_ar)

        This avoids multiple driver errors from individual slave routing.
        """
        lines = []

        lines.append("    // ================================================================")
        lines.append("    // Response MUXes (OR together all slave responses)")
        lines.append("    // ================================================================")
        lines.append("")

        # Generate response muxes for each master
        for master in self.masters:
            # Get unique widths this master connects to
            widths = self._get_connected_slave_widths(master)

            # Generate muxes for each width path
            for width in widths:
                suffix = f"{width}b"

                lines.append(f"    // Master: {master.name}, Width path: {suffix}")

                # Write channel responses (if master has write channels)
                if master.channels in ["wr", "rw"]:
                    lines.extend(self._generate_write_response_mux(master, width, suffix))

                # Read channel responses (if master has read channels)
                if master.channels in ["rd", "rw"]:
                    lines.extend(self._generate_read_response_mux(master, width, suffix))

                lines.append("")

        return lines

    def _generate_write_response_mux(self, master: MasterConfig, width: int,
                                     suffix: str) -> List[str]:
        """Generate write response MUX (awready, wready, b.*) using slave prefixes."""
        lines = []

        # Helper to get slave signal prefix using SignalNaming convention
        def get_slave_prefix(slave: SlaveInfo) -> str:
            return f"{slave.name}_axi_"

        # Find slaves at this width that master connects to
        connected_slaves = []
        for slave_idx in master.slave_connections:
            if self.slaves[slave_idx].data_width == width:
                connected_slaves.append((slave_idx, self.slaves[slave_idx]))

        if not connected_slaves:
            return lines

        # Get master index for bridge_id matching
        master_idx = self.masters.index(master)

        # Generate awready MUX (gated by address re-decode -- same fix
        # as the AW signal gating in _generate_*_channel_routing).
        lines.append(f"    assign {master.name}_{suffix}_awready = ")
        mux_terms = []
        for slave_idx, slave in connected_slaves:
            prefix = get_slave_prefix(slave)
            mux_terms.append(f"        ({master.name}_{suffix}_aw_to_{slave.name} ? {prefix}awready : '0)")
        lines.append(" |\n".join(mux_terms) + ";")
        lines.append("")

        # Generate wready MUX -- W beats follow the AW-to-slave FIFO head.
        lines.append(f"    assign {master.name}_{suffix}_wready = ")
        mux_terms = []
        for slave_idx, slave in connected_slaves:
            prefix = get_slave_prefix(slave)
            mux_terms.append(f"        ({master.name}_{suffix}_w_to_{slave.name} ? {prefix}wready : '0)")
        lines.append(" |\n".join(mux_terms) + ";")
        lines.append("")

        # Generate B channel MUXes (bid, bresp, bvalid) - uses bridge_id for response routing
        lines.append(f"    assign {master.name}_{suffix}_b.id = ")
        mux_terms = []
        for slave_idx, slave in connected_slaves:
            prefix = get_slave_prefix(slave)
            mux_terms.append(f"        (({prefix}bid_bridge_id == {master_idx}) && {prefix}bid_valid ? {prefix}bid : '0)")
        lines.append(" |\n".join(mux_terms) + ";")
        lines.append("")

        lines.append(f"    assign {master.name}_{suffix}_b.resp = ")
        mux_terms = []
        for slave_idx, slave in connected_slaves:
            prefix = get_slave_prefix(slave)
            mux_terms.append(f"        (({prefix}bid_bridge_id == {master_idx}) && {prefix}bid_valid ? {prefix}bresp : '0)")
        lines.append(" |\n".join(mux_terms) + ";")
        lines.append("")

        lines.append(f"    assign {master.name}_{suffix}_bvalid = ")
        mux_terms = []
        for slave_idx, slave in connected_slaves:
            prefix = get_slave_prefix(slave)
            mux_terms.append(f"        (({prefix}bid_bridge_id == {master_idx}) && {prefix}bid_valid ? {prefix}bvalid : '0)")
        lines.append(" |\n".join(mux_terms) + ";")
        lines.append("")

        return lines

