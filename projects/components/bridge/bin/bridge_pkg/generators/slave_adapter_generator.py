"""
Slave Adapter Generator for Bridge Crossbars

Generates per-slave adapter modules that sit between crossbar and external slaves.

Each slave adapter contains:
- Timing isolation wrapper (axi4_master_wr/rd for AXI4 slaves)
- Protocol conversion (axi4_to_apb, axi4_to_axil for non-AXI4 slaves)
- Final skid buffers before external slave interface
"""

from dataclasses import dataclass
from typing import List
from ..signal_naming import SignalNaming, Direction, AXI4Channel, PortDirection, SignalInfo, AXI4_MASTER_SIGNALS


@dataclass
class SlaveInfo:
    """Configuration for a slave port."""
    name: str
    prefix: str
    base_addr: int
    addr_range: int
    data_width: int
    addr_width: int
    protocol: str = 'axi4'  # 'axi4', 'apb', or 'axil'
    enable_ooo: bool = False  # Slave supports out-of-order responses (use CAM vs FIFO)
    use_monitor: bool = True  # Per-port USE_MONITOR override (see PortSpec)


class SlaveAdapterGenerator:
    """
    Generates per-slave adapter modules for bridge crossbars.

    Each slave adapter contains:
    - axi4_master_wr/rd timing wrappers (for AXI4 slaves)
    - axi4_to_apb converter (for APB slaves)
    - axi4_to_axil converter (for AXI4-Lite slaves)
    - Final skid buffers before external interface
    """

    def __init__(self, bridge_name: str, slave_config: SlaveInfo, channels: str,
                 id_width: int = 4, data_width: int = 32,
                 enable_monitoring: bool = False, slave_index: int = 0):
        """
        Initialize slave adapter generator.

        Args:
            bridge_name: Name of bridge (e.g., "bridge_1x2_wr")
            slave_config: Configuration for this slave
            channels: Channel type ("wr", "rd", or "rw")
            id_width: AXI4 ID width (crossbar internal width)
            data_width: Crossbar internal data width
            enable_monitoring: Use axi4_master_*_mon timing-wrapper variants
                (only affects AXI4 slaves -- APB/AXIL shims are unchanged).
            slave_index: Index of this slave (0..N-1). When monitoring is
                on, used to derive the per-port AGENT_ID so each slave-side
                wrapper produces uniquely identifiable monbus packets.
        """
        self.bridge_name = bridge_name
        self.slave = slave_config
        self.channels = channels
        self.id_width = id_width
        self.data_width = data_width
        self.enable_monitoring = enable_monitoring
        self.slave_index = slave_index

        # Determine which channels we have
        self.has_write = channels in ["wr", "rw"]
        self.has_read = channels in ["rd", "rw"]

        # Skid buffer depths (configurable)
        self.skid_depth_aw = 2
        self.skid_depth_ar = 2
        self.skid_depth_w = 4
        self.skid_depth_r = 2
        self.skid_depth_b = 2

    def generate(self) -> str:
        """
        Generate complete slave adapter module.

        Returns:
            SystemVerilog slave adapter module source code
        """
        lines = []

        # Header
        lines.extend(self._generate_header())

        # Module declaration
        lines.extend(self._generate_module_declaration())

        # Internal signals
        lines.extend(self._generate_internal_signals())

        # Bridge ID tracking (CAM or FIFO)
        lines.extend(self._generate_bridge_id_tracking())

        # Timing wrapper or protocol converter
        if self.slave.protocol == 'axi4':
            lines.extend(self._generate_axi4_timing_wrapper())
        elif self.slave.protocol == 'apb':
            lines.extend(self._generate_apb_converter())
        elif self.slave.protocol == 'axil':
            # Real AXI4-to-AXI4-Lite conversion at the slave boundary.
            # The crossbar carries full AXI4; only when the packet is
            # about to land on an AXIL slave do we shim it through
            # axi4_to_axil4_{rd,wr} (which lives in projects/components
            # /converters/rtl/). This matches the user's architectural
            # intent: one AXI4 fabric, protocol-specific shims at the
            # last mile.
            lines.extend(self._generate_axil_converter())

        # Module end
        lines.append(f"endmodule : {self.slave.name}_adapter")
        lines.append("")

        return "\n".join(lines)

    def _generate_header(self) -> List[str]:
        """Generate file header."""
        return [
            f"// Slave Adapter: {self.slave.name}_adapter",
            "// Generated by: SlaveAdapterGenerator",
            "//",
            f"// Provides timing isolation and protocol conversion for slave: {self.slave.name}",
            f"// Protocol: {self.slave.protocol.upper()}",
            "",
            "`timescale 1ns / 1ps",
            "",
            f"import {self.bridge_name}_pkg::*;",
            ""
        ]

    def _generate_module_declaration(self) -> List[str]:
        """Generate module declaration with parameters and all ports."""
        lines = []

        # Module declaration. ID_WIDTH defaults to the bridge's actual
        # crossbar id_width (= max of all master id_widths) — the previous
        # default of 4 silently shadowed any wider master. BRIDGE_ID_WIDTH
        # is dropped from the parameter list and comes from the imported
        # package (`import {bridge}_pkg::*;`), where it's set to
        # ceil(log2(NUM_MASTERS)). Earlier the adapter declared its own
        # `parameter int BRIDGE_ID_WIDTH = 1` which shadowed the package
        # value and clipped multi-master bridges to a 1-bit routing tag.
        # Adapter parameters. ID_WIDTH always present; USE_MONITOR_WR
        # and USE_MONITOR_RD are emitted only when the variant is
        # monitored (enable_monitoring=True) so non-mon bridges keep a
        # clean port list. Defaults come from the TOML's per-slave
        # `use_monitor` field but the bridge top always overrides them
        # with the effective-after-global-knobs value, so the default
        # only matters for standalone instantiation. The wrapper's
        # `.USE_MONITOR(...)` override references these parameters by
        # name so the bridge top can flip them at integration time
        # without regenerating.
        lines.append(f"module {self.slave.name}_adapter #(")
        lines.append(f"    parameter int ID_WIDTH = {self.id_width}")
        if self.enable_monitoring:
            default_lit = "1'b1" if self.slave.use_monitor else "1'b0"
            if self.has_write:
                lines.append(f"   ,parameter bit USE_MONITOR_WR = {default_lit}")
            if self.has_read:
                lines.append(f"   ,parameter bit USE_MONITOR_RD = {default_lit}")
        lines.append(f") (")
        lines.append("    input  logic aclk,")
        lines.append("    input  logic aresetn,")
        lines.append("")

        # Crossbar-facing interface (AXI4 input from crossbar)
        lines.append(f"    // Crossbar interface (AXI4 from crossbar)")
        lines.extend(self._generate_crossbar_interface_ports())

        # Bridge ID tracking signals
        lines.append("")
        lines.append(f"    // Bridge ID tracking signals")
        lines.extend(self._generate_bridge_id_ports())
        lines.append("")

        # External slave interface
        lines.append(f"    // External slave interface ({self.slave.protocol.upper()})")
        lines.extend(self._generate_external_interface_ports())

        # Monitor side-band: AXI4 slaves use _mon timing wrappers on the
        # external path; AXIL/APB slaves insert an axi4_master_*_mon on
        # the crossbar-side AXI4 path upstream of their converter shim
        # (the fabric is always AXI4, so the same Axi4TimingWrapper +
        # MonitoredWrapper plumbing covers every protocol).
        if self.enable_monitoring:
            from .adapter_generator import _ensure_trailing_comma
            _ensure_trailing_comma(lines)
            lines.append("")
            lines.extend(self._generate_monitor_ports())

        lines.append(");")
        lines.append("")

        return lines

    def _generate_monitor_ports(self) -> List[str]:
        """Per-wrapper monbus output + cfg input ports for this slave
        adapter. Mirrors AdapterGenerator._generate_monitor_ports() so
        the bridge top can wire master/slave adapters with the same
        connector-naming scheme.

        After the 64->128-bit packet widening, every channel also gets a
        64-bit `monbus_<chan>_timestamp` side-band output (paired with
        the packet). A single shared `i_mon_time` input is declared once
        per adapter (not per channel) because every wrapper instance
        consumes the same free-running counter from monbus_axil_group's
        `mon_time_out`."""
        from ..components.axi4_timing_wrapper_component import Axi4TimingWrapper
        from .adapter_generator import _MONITOR_CFG_WIDTHS

        lines: List[str] = []
        channels: List[str] = []
        if self.channels in ("wr", "rw"):
            channels.append("wr")
        if self.channels in ("rd", "rw"):
            channels.append("rd")

        # Shared free-running monitor-time -- one input shared by every
        # internal wrapper instance.
        if channels:
            lines.append("    // Shared free-running monitor-time (from monbus_axil_group.mon_time_out)")
            lines.append("    input  monitor_common_pkg::monbus_timestamp_t i_mon_time,")
            lines.append("")

        last_chan = channels[-1] if channels else None
        for chan in channels:
            lines.append(f"    // Monitor side-band: {chan} wrapper")
            lines.append(f"    output logic                                  monbus_{chan}_valid,")
            lines.append(f"    input  logic                                  monbus_{chan}_ready,")
            lines.append(f"    output monitor_common_pkg::monitor_packet_t   monbus_{chan}_packet,")
            lines.append(f"    output monitor_common_pkg::monbus_timestamp_t monbus_{chan}_timestamp,")
            lines.append("")
            for i, sig in enumerate(Axi4TimingWrapper.MONITOR_CFG_SIGNALS):
                is_final_cfg = (chan == last_chan and i == len(Axi4TimingWrapper.MONITOR_CFG_SIGNALS) - 1)
                width = _MONITOR_CFG_WIDTHS[sig]
                width_decl = "       " if width == 1 else f"[{width-1}:0]"
                base = sig[len("cfg_"):]
                sep = "" if is_final_cfg else ","
                lines.append(f"    input  logic {width_decl} cfg_{chan}_{base}{sep}")
            if chan != last_chan:
                lines.append("")
        return lines

    def _generate_crossbar_interface_ports(self) -> List[str]:
        """
        Generate crossbar-facing interface ports (AXI4 inputs from crossbar).

        These are always AXI4 regardless of slave protocol.
        Port directions are from adapter's perspective:
        - Adapter receives requests from crossbar (INPUT)
        - Adapter sends responses to crossbar (OUTPUT)
        """
        lines = []

        # Use crossbar output naming: xbar_{slave}_axi_*
        # This matches what bridge_module_generator expects
        prefix = f"xbar_{self.slave.name}_axi_"

        # Width parameters. self.id_width is the crossbar/master id_width
        # passed in by the caller (== max master id_width). Was hardcoded
        # to 4 — Bug B in TASK-011.
        width_values = {
            'ID_WIDTH': self.id_width,
            'ADDR_WIDTH': self.slave.addr_width,
            'DATA_WIDTH': self.slave.data_width,
            'STRB_WIDTH': self.slave.data_width // 8,
            'USER_WIDTH': 1
        }

        # Determine channels based on adapter type
        channels = []
        if self.channels in ["wr", "rw"]:
            channels.extend([AXI4Channel.AW, AXI4Channel.W, AXI4Channel.B])
        if self.channels in ["rd", "rw"]:
            channels.extend([AXI4Channel.AR, AXI4Channel.R])

        # Get signal info from SignalNaming
        # Crossbar is master to adapter, so adapter sees inverted directions
        signal_db = AXI4_MASTER_SIGNALS

        for channel in channels:
            if channel not in signal_db:
                continue

            for sig_info in signal_db[channel]:
                # Build signal name
                sig_name = f"{prefix}{channel.value}{sig_info.name}"

                # Invert direction (adapter receives from crossbar master)
                inverted_direction = PortDirection.INPUT if sig_info.direction == PortDirection.OUTPUT else PortDirection.OUTPUT

                # Create inverted signal info
                inverted_sig = SignalInfo(
                    name=sig_info.name,
                    direction=inverted_direction,
                    width_expr=sig_info.width_expr,
                    width_param=sig_info.width_param,
                    is_vector=sig_info.is_vector,
                    description=sig_info.description
                )

                # Get declaration
                declaration = inverted_sig.get_declaration(sig_name, width_values)
                lines.append(f"    {declaration},")

        return lines

    def _generate_bridge_id_ports(self) -> List[str]:
        """
        Generate bridge ID tracking ports.

        Inputs: bridge_id_aw, bridge_id_ar (from crossbar, indicating master source)
        Outputs: bid_bridge_id, rid_bridge_id (to crossbar, for response routing)
        """
        lines = []

        # BRIDGE_ID_WIDTH will be defined in package
        if self.has_write:
            lines.append("    input  logic [BRIDGE_ID_WIDTH-1:0] xbar_bridge_id_aw,")
            lines.append("    output logic [BRIDGE_ID_WIDTH-1:0] bid_bridge_id,")
            lines.append("    output logic                       bid_valid,")

        if self.has_read:
            if self.has_write:
                lines.append("")
            lines.append("    input  logic [BRIDGE_ID_WIDTH-1:0] xbar_bridge_id_ar,")
            lines.append("    output logic [BRIDGE_ID_WIDTH-1:0] rid_bridge_id,")
            lines.append("    output logic                       rid_valid,")

        return lines

    def _generate_external_interface_ports(self) -> List[str]:
        """
        Generate external slave interface ports.

        Protocol depends on slave type:
        - AXI4: Direct AXI4 signals (from axi4_master_wr/rd wrapper)
        - APB: APB signals
        - AXIL: AXI4-Lite signals
        """
        lines = []

        if self.slave.protocol == 'axi4':
            lines.extend(self._generate_axi4_external_ports())
        elif self.slave.protocol == 'apb':
            lines.extend(self._generate_apb_external_ports())
        elif self.slave.protocol == 'axil':
            # Real AXI4-Lite external ports (no id/len/size/burst/last/user).
            # See _generate_axil_converter for the shim that converts
            # the crossbar-side AXI4 into AXI4-Lite at this boundary.
            lines.extend(self._generate_axil_external_ports())

        return lines

    def _generate_axi4_external_ports(self) -> List[str]:
        """Generate AXI4 external slave ports using SignalNaming."""
        lines = []

        # Use slave prefix from config
        prefix = self.slave.prefix

        # Width parameters. ID_WIDTH is master pass-through (Bug B fix).
        width_values = {
            'ID_WIDTH': self.id_width,
            'ADDR_WIDTH': self.slave.addr_width,
            'DATA_WIDTH': self.slave.data_width,
            'STRB_WIDTH': self.slave.data_width // 8,
            'USER_WIDTH': 1
        }

        # Determine channels
        channels = []
        if self.channels in ["wr", "rw"]:
            channels.extend([AXI4Channel.AW, AXI4Channel.W, AXI4Channel.B])
        if self.channels in ["rd", "rw"]:
            channels.extend([AXI4Channel.AR, AXI4Channel.R])

        # Adapter acts as master to external slave
        signal_db = AXI4_MASTER_SIGNALS

        # Pre-calculate last signal
        last_channel = channels[-1] if channels else None

        for channel_idx, channel in enumerate(channels):
            if channel not in signal_db:
                continue

            channel_signals = list(signal_db[channel])
            for sig_idx, sig_info in enumerate(channel_signals):
                # Build signal name
                sig_name = f"{prefix}{channel.value}{sig_info.name}"

                # Get declaration
                declaration = sig_info.get_declaration(sig_name, width_values)

                # Check if last signal
                is_last_channel = (channel == last_channel)
                is_last_signal = (sig_idx == len(channel_signals) - 1)
                is_very_last = (is_last_channel and is_last_signal)

                if is_very_last:
                    lines.append(f"    {declaration}")  # No comma
                else:
                    lines.append(f"    {declaration},")

        return lines

    def _generate_apb_external_ports(self) -> List[str]:
        """Generate APB external slave ports."""
        lines = []
        prefix = self.slave.prefix

        # APB signals (adapter is master to slave)
        addr_width = self.slave.addr_width
        data_width = self.slave.data_width
        strb_width = data_width // 8

        # APB standard uses uppercase signal names (PADDR, PSEL, etc.)
        lines.append(f"    output logic [{addr_width-1}:0] {prefix}PADDR,")
        lines.append(f"    output logic                  {prefix}PSEL,")
        lines.append(f"    output logic                  {prefix}PENABLE,")
        lines.append(f"    output logic                  {prefix}PWRITE,")
        lines.append(f"    output logic [{data_width-1}:0] {prefix}PWDATA,")
        lines.append(f"    output logic [{strb_width-1}:0] {prefix}PSTRB,")
        lines.append(f"    output logic [2:0]            {prefix}PPROT,")
        lines.append(f"    input  logic [{data_width-1}:0] {prefix}PRDATA,")
        lines.append(f"    input  logic                  {prefix}PSLVERR,")
        lines.append(f"    input  logic                  {prefix}PREADY")

        return lines

    def _generate_axil_external_ports(self) -> List[str]:
        """Generate AXI4-Lite external slave ports."""
        lines = []
        prefix = self.slave.prefix

        # AXI4-Lite signals (adapter is master to slave)
        addr_width = self.slave.addr_width
        data_width = self.slave.data_width
        strb_width = data_width // 8

        # Determine which channels to generate
        has_write = self.channels in ["wr", "rw"]
        has_read = self.channels in ["rd", "rw"]

        # Track if we need comma after signals
        signals_added = False

        if has_write:
            # Write Address Channel
            lines.append(f"    output logic [{addr_width-1}:0] {prefix}awaddr,")
            lines.append(f"    output logic [2:0]            {prefix}awprot,")
            lines.append(f"    output logic                  {prefix}awvalid,")
            lines.append(f"    input  logic                  {prefix}awready,")
            lines.append(f"    // Write Data Channel")
            lines.append(f"    output logic [{data_width-1}:0] {prefix}wdata,")
            lines.append(f"    output logic [{strb_width-1}:0] {prefix}wstrb,")
            lines.append(f"    output logic                  {prefix}wvalid,")
            lines.append(f"    input  logic                  {prefix}wready,")
            lines.append(f"    // Write Response Channel")
            lines.append(f"    input  logic [1:0]            {prefix}bresp,")
            lines.append(f"    input  logic                  {prefix}bvalid,")
            # Comma only if there are read channels following
            if has_read:
                lines.append(f"    output logic                  {prefix}bready,")
            else:
                lines.append(f"    output logic                  {prefix}bready")  # Last signal - no comma
            signals_added = True

        if has_read:
            if signals_added:
                lines.append(f"    // Read Address Channel")
            lines.append(f"    output logic [{addr_width-1}:0] {prefix}araddr,")
            lines.append(f"    output logic [2:0]            {prefix}arprot,")
            lines.append(f"    output logic                  {prefix}arvalid,")
            lines.append(f"    input  logic                  {prefix}arready,")
            lines.append(f"    // Read Data Channel")
            lines.append(f"    input  logic [{data_width-1}:0] {prefix}rdata,")
            lines.append(f"    input  logic [1:0]            {prefix}rresp,")
            lines.append(f"    input  logic                  {prefix}rvalid,")
            lines.append(f"    output logic                  {prefix}rready")  # Last signal - no comma

        return lines

    def _generate_internal_signals(self) -> List[str]:
        """Generate internal signal declarations."""
        lines = [
            "    // ================================================================",
            "    // Internal Signals",
            "    // ================================================================",
            ""
        ]

        # Add protocol converter intermediate signals for APB/AXIL
        # These allow FIFO tracking to monitor converter output instead of crossbar input
        if self.slave.protocol in ['apb', 'axil']:
            lines.append("    // Protocol converter intermediate signals")
            lines.append("    // (FIFO tracking monitors these instead of crossbar signals)")
            if self.has_write:
                lines.append("    logic converter_bvalid;")
                lines.append("    logic converter_bready;")
                lines.append("")
            if self.has_read:
                lines.append("    logic converter_rvalid;")
                lines.append("    logic converter_rready;")
                lines.append("    logic converter_rlast;")
                lines.append("")

        # Add FIFO/CAM internal signals if needed
        # Handle missing enable_ooo attribute (default to False for in-order)
        enable_ooo = getattr(self.slave, 'enable_ooo', False)

        if enable_ooo:
            lines.append("    // CAM tracking signals (out-of-order mode)")
        else:
            lines.append("    // FIFO tracking signals (in-order mode)")

        if self.has_write:
            lines.append("    logic cam_wr_allocate;")
            lines.append("    logic cam_wr_deallocate;")
            lines.append("    logic [ID_WIDTH-1:0] cam_wr_allocate_tag;")
            lines.append("    logic [BRIDGE_ID_WIDTH-1:0] cam_wr_allocate_data;")
            lines.append("    logic [ID_WIDTH-1:0] cam_wr_deallocate_tag;")
            lines.append("")

        if self.has_read:
            lines.append("    logic cam_rd_allocate;")
            lines.append("    logic cam_rd_deallocate;")
            lines.append("    logic [ID_WIDTH-1:0] cam_rd_allocate_tag;")
            lines.append("    logic [BRIDGE_ID_WIDTH-1:0] cam_rd_allocate_data;")
            lines.append("    logic [ID_WIDTH-1:0] cam_rd_deallocate_tag;")
            lines.append("")

        return lines

    def _generate_bridge_id_tracking(self) -> List[str]:
        """
        Generate bridge ID tracking logic (CAM or FIFO).

        Returns:
            SystemVerilog for CAM/FIFO instantiation and control logic
        """
        lines = []
        crossbar_prefix = f"xbar_{self.slave.name}_axi_"
        slave_prefix = self.slave.prefix

        # Handle missing enable_ooo attribute (default to False for in-order)
        enable_ooo = getattr(self.slave, 'enable_ooo', False)
        if not hasattr(self.slave, 'enable_ooo'):
            print(f"  ⚠ Slave '{self.slave.name}' missing enable_ooo field - defaulting to in-order (FIFO mode)")

        lines.append("    // ================================================================")
        if enable_ooo:
            lines.append("    // Bridge ID Tracking - CAM Mode (Out-of-Order)")
        else:
            lines.append("    // Bridge ID Tracking - FIFO Mode (In-Order)")
        lines.append("    // ================================================================")
        lines.append("")

        # Generate write channel tracking
        if self.has_write:
            if enable_ooo:
                lines.extend(self._generate_cam_write_tracking(crossbar_prefix, slave_prefix))
            else:
                lines.extend(self._generate_fifo_write_tracking(crossbar_prefix, slave_prefix))

        # Generate read channel tracking
        if self.has_read:
            if enable_ooo:
                lines.extend(self._generate_cam_read_tracking(crossbar_prefix, slave_prefix))
            else:
                lines.extend(self._generate_fifo_read_tracking(crossbar_prefix, slave_prefix))

        return lines

    def _generate_cam_write_tracking(self, crossbar_prefix: str, slave_prefix: str) -> List[str]:
        """Generate CAM-based write tracking."""
        lines = []

        lines.append("    // Write Channel CAM")
        lines.append("    bridge_cam #(")
        lines.append("        .TAG_WIDTH(ID_WIDTH),")
        lines.append("        .DATA_WIDTH(BRIDGE_ID_WIDTH),")
        lines.append("        .DEPTH(16),")
        lines.append("        .ALLOW_DUPLICATES(1),  // Mode 2: OOO support")
        lines.append("        .PIPELINE_EVICT(0)")
        lines.append("    ) u_wr_cam (")
        lines.append("        .clk(aclk),")
        lines.append("        .rst_n(aresetn),")
        lines.append("")
        lines.append("        // Allocate on AW (from crossbar)")
        lines.append(f"        .allocate({crossbar_prefix}awvalid && {crossbar_prefix}awready),")
        lines.append(f"        .allocate_tag({crossbar_prefix}awid),")
        lines.append("        .allocate_data(xbar_bridge_id_aw),")
        lines.append("")
        lines.append("        // Deallocate on B (from converter)")
        lines.append(f"        .deallocate({crossbar_prefix}bvalid && {crossbar_prefix}bready),")
        lines.append(f"        .deallocate_tag({crossbar_prefix}bid),")
        lines.append("        .deallocate_valid(bid_valid),")
        lines.append("        .deallocate_data(bid_bridge_id),")
        lines.append("")
        lines.append("        // Status (unconnected)")
        lines.append("        .cam_hit(),")
        lines.append("        .tags_empty(),")
        lines.append("        .tags_full()")
        lines.append("    );")
        lines.append("")

        return lines

    def _generate_fifo_write_tracking(self, crossbar_prefix: str, slave_prefix: str) -> List[str]:
        """Generate FIFO-based write tracking."""
        lines = []

        # Determine pop monitoring point based on protocol.
        #   axi4: response comes back from the timing wrapper directly,
        #         so monitor the crossbar-facing handshake.
        #   apb / axil: the shim has its own latency between the xbar
        #         and the external slave; we hand `converter_bvalid` /
        #         `converter_bready` off the shim's AXI4 (s_axi) side
        #         so the FIFO pops the moment the shim actually
        #         produces a response, not when external completes.
        if self.slave.protocol in ('apb', 'axil'):
            label = self.slave.protocol.upper()
            pop_condition = "converter_bvalid && converter_bready"
            lines.append(f"    // Write Channel FIFO (In-Order) - {label} Protocol")
            lines.append("    // NOTE: Monitors converter output (converter_bvalid), not crossbar input")
            lines.append("    //       This ensures FIFO pops when converter actually produces response")
        else:  # axi4
            pop_condition = f"{crossbar_prefix}bvalid && {crossbar_prefix}bready"
            lines.append("    // Write Channel FIFO (In-Order) - AXI4 Protocol")

        lines.append("    localparam WR_FIFO_DEPTH = 16;")
        lines.append("    logic [BRIDGE_ID_WIDTH-1:0] wr_fifo [WR_FIFO_DEPTH];")
        lines.append("    logic [$clog2(WR_FIFO_DEPTH):0] wr_ptr, rd_ptr;")
        lines.append("")
        lines.append("    // Push on AW (crossbar → adapter)")
        lines.append("    always_ff @(posedge aclk or negedge aresetn) begin")
        lines.append("        if (!aresetn) begin")
        lines.append("            wr_ptr <= '0;")
        lines.append(f"        end else if ({crossbar_prefix}awvalid && {crossbar_prefix}awready) begin")
        lines.append("            wr_fifo[wr_ptr[$clog2(WR_FIFO_DEPTH)-1:0]] <= xbar_bridge_id_aw;")
        lines.append("            wr_ptr <= wr_ptr + 1'b1;")
        lines.append("        end")
        lines.append("    end")
        lines.append("")
        lines.append(f"    // Pop on B response ({pop_condition})")
        lines.append("    always_ff @(posedge aclk or negedge aresetn) begin")
        lines.append("        if (!aresetn) begin")
        lines.append("            rd_ptr <= '0;")
        lines.append(f"        end else if ({pop_condition}) begin")
        lines.append("            rd_ptr <= rd_ptr + 1'b1;")
        lines.append("        end")
        lines.append("    end")
        lines.append("")
        lines.append("    // bid_bridge_id / bid_valid drive the crossbar's response mux,")
        lines.append("    // which gates B going BACK to the master on bid_valid. Earlier")
        lines.append("    // versions registered these on the handshake completing — but")
        lines.append("    // the handshake CAN'T complete until the master sees bvalid,")
        lines.append("    // and the master can't see bvalid until bid_valid is high.")
        lines.append("    // Result: deadlock. Drive these combinationally so the route")
        lines.append("    // is open from the moment a B arrives.")
        lines.append("    assign bid_bridge_id = wr_fifo[rd_ptr[$clog2(WR_FIFO_DEPTH)-1:0]];")
        lines.append("    assign bid_valid     = (wr_ptr != rd_ptr);")
        lines.append("")

        return lines

    def _generate_cam_read_tracking(self, crossbar_prefix: str, slave_prefix: str) -> List[str]:
        """Generate CAM-based read tracking."""
        lines = []

        lines.append("    // Read Channel CAM")
        lines.append("    bridge_cam #(")
        lines.append("        .TAG_WIDTH(ID_WIDTH),")
        lines.append("        .DATA_WIDTH(BRIDGE_ID_WIDTH),")
        lines.append("        .DEPTH(16),")
        lines.append("        .ALLOW_DUPLICATES(1),  // Mode 2: OOO support")
        lines.append("        .PIPELINE_EVICT(0)")
        lines.append("    ) u_rd_cam (")
        lines.append("        .clk(aclk),")
        lines.append("        .rst_n(aresetn),")
        lines.append("")
        lines.append("        // Allocate on AR (from crossbar)")
        lines.append(f"        .allocate({crossbar_prefix}arvalid && {crossbar_prefix}arready),")
        lines.append(f"        .allocate_tag({crossbar_prefix}arid),")
        lines.append("        .allocate_data(xbar_bridge_id_ar),")
        lines.append("")
        lines.append("        // Deallocate on R (from converter)")
        lines.append(f"        .deallocate({crossbar_prefix}rvalid && {crossbar_prefix}rready && {crossbar_prefix}rlast),")
        lines.append(f"        .deallocate_tag({crossbar_prefix}rid),")
        lines.append("        .deallocate_valid(rid_valid),")
        lines.append("        .deallocate_data(rid_bridge_id),")
        lines.append("")
        lines.append("        // Status (unconnected)")
        lines.append("        .cam_hit(),")
        lines.append("        .tags_empty(),")
        lines.append("        .tags_full()")
        lines.append("    );")
        lines.append("")

        return lines

    def _generate_fifo_read_tracking(self, crossbar_prefix: str, slave_prefix: str) -> List[str]:
        """Generate FIFO-based read tracking."""
        lines = []

        # Determine pop monitoring point based on protocol -- same
        # reasoning as the write FIFO. APB and AXIL slaves go through
        # shims with their own latency; tracking the shim's AXI4
        # (s_axi) output via converter_rvalid keeps the FIFO in
        # lockstep with the actual response.
        if self.slave.protocol in ('apb', 'axil'):
            label = self.slave.protocol.upper()
            pop_condition = "converter_rvalid && converter_rready && converter_rlast"
            lines.append(f"    // Read Channel FIFO (In-Order) - {label} Protocol")
            lines.append("    // NOTE: Monitors converter output (converter_rvalid), not crossbar input")
            lines.append("    //       This ensures FIFO pops when converter actually produces response")
        else:  # axi4
            pop_condition = f"{crossbar_prefix}rvalid && {crossbar_prefix}rready && {crossbar_prefix}rlast"
            lines.append("    // Read Channel FIFO (In-Order) - AXI4 Protocol")

        lines.append("    localparam RD_FIFO_DEPTH = 16;")
        lines.append("    logic [BRIDGE_ID_WIDTH-1:0] rd_fifo [RD_FIFO_DEPTH];")
        lines.append("    logic [$clog2(RD_FIFO_DEPTH):0] ar_ptr, r_ptr;")
        lines.append("")
        lines.append("    // Push on AR (crossbar → adapter)")
        lines.append("    always_ff @(posedge aclk or negedge aresetn) begin")
        lines.append("        if (!aresetn) begin")
        lines.append("            ar_ptr <= '0;")
        lines.append(f"        end else if ({crossbar_prefix}arvalid && {crossbar_prefix}arready) begin")
        lines.append("            rd_fifo[ar_ptr[$clog2(RD_FIFO_DEPTH)-1:0]] <= xbar_bridge_id_ar;")
        lines.append("            ar_ptr <= ar_ptr + 1'b1;")
        lines.append("        end")
        lines.append("    end")
        lines.append("")
        lines.append(f"    // Pop on R response ({pop_condition})")
        lines.append("    always_ff @(posedge aclk or negedge aresetn) begin")
        lines.append("        if (!aresetn) begin")
        lines.append("            r_ptr <= '0;")
        lines.append(f"        end else if ({pop_condition}) begin")
        lines.append("            r_ptr <= r_ptr + 1'b1;")
        lines.append("        end")
        lines.append("    end")
        lines.append("")
        lines.append("    // rid_bridge_id / rid_valid drive the crossbar's response mux,")
        lines.append("    // which gates R going BACK to the master on rid_valid. Earlier")
        lines.append("    // versions registered these on the handshake completing — but")
        lines.append("    // the handshake CAN'T complete until the master sees rvalid,")
        lines.append("    // and the master can't see rvalid until rid_valid is high.")
        lines.append("    // Result: deadlock. Drive these combinationally so the route")
        lines.append("    // is open from the moment an R arrives.")
        lines.append("    assign rid_bridge_id = rd_fifo[r_ptr[$clog2(RD_FIFO_DEPTH)-1:0]];")
        lines.append("    assign rid_valid     = (ar_ptr != r_ptr);")
        lines.append("")

        return lines

    def _generate_axi4_timing_wrapper(self) -> List[str]:
        """Generate axi4_master_wr/rd timing wrapper instantiation."""
        lines = []

        crossbar_prefix = f"xbar_{self.slave.name}_axi_"
        slave_prefix = self.slave.prefix

        if self.channels in ["wr", "rw"]:
            lines.extend(self._generate_master_wr_wrapper(crossbar_prefix, slave_prefix))

        if self.channels in ["rd", "rw"]:
            lines.extend(self._generate_master_rd_wrapper(crossbar_prefix, slave_prefix))

        return lines

    def _generate_master_wr_wrapper(self, crossbar_prefix: str, slave_prefix: str) -> List[str]:
        """Generate axi4_master_wr timing-wrapper instance via the
        typed Axi4TimingWrapper component."""
        from ..components.axi4_timing_wrapper_component import Axi4TimingWrapper

        # Monitor identity: UNIT_ID=1 marks every slave-side wrapper
        # (axi4_master_*_mon -- the bridge looks like a master to the
        # external slave); AGENT_ID = (slave_index << 4) | 0x1 for wr.
        # Matches the SV defaults' UNIT_ID=1 family.
        unit_id = 1 if self.enable_monitoring else None
        agent_id = ((self.slave_index << 4) | 0x1) if self.enable_monitoring else None

        wrapper = Axi4TimingWrapper(
            side='master', channel='wr',
            instance_name='u_master_wr',
            id_width=self.id_width,
            addr_width=self.slave.addr_width,
            data_width=self.slave.data_width,
            skid_depth_ax=str(self.skid_depth_aw),
            skid_depth_data=str(self.skid_depth_w),
            skid_depth_resp=str(self.skid_depth_b),
            mon=self.enable_monitoring,
            unit_id=unit_id,
            agent_id=agent_id,
            # Reference the adapter-level parameter so the bridge top
            # can override it at instantiation without regenerating.
            use_monitor_param=(
                'USE_MONITOR_WR' if self.enable_monitoring else None
            ),
        )
        wrapper.connect_clocks_and_resets()
        wrapper.connect_bridge_internal(connector_prefix=crossbar_prefix)
        wrapper.connect_external(connector_prefix=slave_prefix)
        wrapper.add_status()
        if self.enable_monitoring:
            wrapper.connect_monbus(
                valid='monbus_wr_valid',
                ready='monbus_wr_ready',
                packet='monbus_wr_packet',
                timestamp='monbus_wr_timestamp',
                mon_time='i_mon_time',
            )
            wrapper.connect_cfg(connector_prefix='cfg_wr_')
            wrapper.add_addr_range_tieoff()
        return ["    // AXI4 Master Write Timing Wrapper"] + wrapper.generate_lines()

